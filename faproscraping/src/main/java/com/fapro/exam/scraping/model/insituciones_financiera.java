package com.fapro.exam.scraping.model;

import java.util.Date;


public class insituciones_financiera {
    
    private Integer no;
    private String razon_social;
    private String pais;
    private String datos_inscripcion;
    private String vigencia;
    private String datos_ultima_actualizacion;
    private String estado_actualizado;

    /**
     * @return the no
     */
    public Integer getNo() {
        return no;
    }

    /**
     * @param no the no to set
     */
    public void setNo(Integer no) {
        this.no = no;
    }

    /**
     * @return the razon_social
     */
    public String getRazon_social() {
        return razon_social;
    }

    /**
     * @param razon_social the razon_social to set
     */
    public void setRazon_social(String razon_social) {
        this.razon_social = razon_social;
    }

    /**
     * @return the pais
     */
    public String getPais() {
        return pais;
    }

    /**
     * @param pais the pais to set
     */
    public void setPais(String pais) {
        this.pais = pais;
    }

    /**
     * @return the datos_inscripcion
     */
    public String getDatos_inscripcion() {
        return datos_inscripcion;
    }

    /**
     * @param datos_inscripcion the datos_inscripcion to set
     */
    public void setDatos_inscripcion(String datos_inscripcion) {
        this.datos_inscripcion = datos_inscripcion;
    }

    /**
     * @return the vigencia
     */
    public String getVigencia() {
        return vigencia;
    }

    /**
     * @param vigencia the vigencia to set
     */
    public void setVigencia(String vigencia) {
        this.vigencia = vigencia;
    }

    /**
     * @return the datos_ultima_actualizacion
     */
    public String getDatos_ultima_actualizacion() {
        return datos_ultima_actualizacion;
    }

    /**
     * @param datos_ultima_actualizacion the datos_ultima_actualizacion to set
     */
    public void setDatos_ultima_actualizacion(String datos_ultima_actualizacion) {
        this.datos_ultima_actualizacion = datos_ultima_actualizacion;
    }

    /**
     * @return the estado_actualizado
     */
    public String getEstado_actualizado() {
        return estado_actualizado;
    }

    /**
     * @param estado_actualizado the estado_actualizado to set
     */
    public void setEstado_actualizado(String estado_actualizado) {
        this.estado_actualizado = estado_actualizado;
    }
   
    
    
}
